x = 3.14
y = -4
z = 5
#Rounding
result1 = round(x)
print(result1)
#Absolute Value
result2 = abs(y)
print(result2)
#Power Function
result3 = pow(4, 3)
print(result3)
#Maximum value
result4 = max(x, y, z)
print(result4)
#Minimum value
result5 = min(x, y, z)
print(result5)

#For more function
import math
#PI
print(math.pi)
#Exponential
print(math.e)
#Square Root
result6 = math.sqrt(9)
print(result6)
#Ceiling (Round Up)
result7 = math.ceil(9.05)
print(result7)
#Floor (Round Down)
result8 = math.floor(9.9)
print(result8)